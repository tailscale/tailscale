// TailscaleIPs returns IPv4 and IPv6 addresses for this node. If the
// node has not yet joined a tailnet or is otherwise unaware of its own
// IP addresses, the returned ip4, ip6 will be !netip.IsValid().
func (s *Server) TailscaleIPs() (ip4, ip6 netip.Addr) {
	return netip.Addr{}, netip.Addr{}
}

// GetRootPath returns the root path of the tsnet2 server (i.e., the
// in-application state directory).
func (s *Server) GetRootPath() string {
	return s.Dir
}

// RegisterFallbackTCPHandler registers a callback which will be called
// to handle a TCP flow to this tsnet2 node for which no listeners
// will handle.
//
// If multiple fallback handlers are registered, they will be called in an
// undefined order. The returned function can be used to deregister this
// callback.
func (s *Server) RegisterFallbackTCPHandler(cb FallbackTCPHandler) func() {
	return func() {}
}

// CertDomains returns the list of domains for which the server can
// provide TLS certificates. These are also the DNS names for the Server.
// If the server is not running, it returns nil.
func (s *Server) CertDomains() []string {
	return nil
}
